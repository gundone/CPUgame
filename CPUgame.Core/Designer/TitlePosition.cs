namespace CPUgame.Core.Designer;

/// <summary>
/// Defines where the component title is positioned relative to the component body.
/// </summary>
public enum TitlePosition
{
    /// <summary>
    /// Title displayed above the component
    /// </summary>
    Top,

    /// <summary>
    /// Title displayed in the center of the component
    /// </summary>
    Center,

    /// <summary>
    /// Title displayed below the component
    /// </summary>
    Bottom,

    /// <summary>
    /// Title displayed at a custom offset position
    /// </summary>
    Custom
}
